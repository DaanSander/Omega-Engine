package com.daansander.engine.math;

/**
 * Created by Daan on 3-10-2015.
 */
public class Vector2D {

    private final int X;
    private final int Y;

    public Vector2D(int x, int y) {
        X = x;
        Y = y;
    }

    public int getY() {
        return Y;
    }

    public int getX() {
        return X;
    }
}
